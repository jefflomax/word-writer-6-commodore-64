;
SPACE_OUT:
 LDA #' '
 DB $2C		;SKIP 2
CRLF:
 LDA #$0D       ;SEND CR
CHROUT:
;INTERFACE TO PRINT PREVIEW
 BIT PREVIEW    ;ARE WE IN PREVIEW MODE?
 BPL CHROUT1    ;[0] NO, DO REGULAR CHROUT
 JMP DRAWCHAR   ;PLACE CHAR ON 80 COLUMN SCREEN
;
CHROUT1:
 JSR ROMSIN
 JSR $FFD2
 JMP ROMOUT
;
CLALL:
 JSR ROMSIN
 JSR $FFE7
 JMP ROMOUT     
;
CLOSE:
