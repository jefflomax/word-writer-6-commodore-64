 JSR $FFC3
 JMP ROMOUT 
;
CLRCHN:
 JSR ROMSIN
 JSR $FFCC
 JMP ROMOUT
;
.IF CALCULATOR_PRESENT
CRUNCH:
 JSR ROMSIN
