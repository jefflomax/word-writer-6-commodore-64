 DB 0,0,0
;
; BRANCH TO WORD WRITER
;
BEGIN
;
 JMP STRTUP ;HELLO WORD WRITER (ENDMOD)
;
 IFTRUE DEBUGGER_PRESENT
 NOP
 ELSE
PAGE_TABLE:	EQU $033C+$1F
PAGE_LENGTH_LO:	EQU $033C+$1F+53
PAGE_LENGTH_HI: EQU $033C+$1F+53+53
;
 ENDIF
;
;
; WINDOW ROUTINES
;
ROMSIN:
 PHA         ;SAVE AC
 LDA #$37    ;GET BASIC/IO/KERNAL
 STA $01     ;INTO CONTEXT
;
 IF DEBUGGER_PRESENT
 NOP
 ELSE
 CLI         ;START INTERRUPTS
 ENDIF
;
 PLA         ;RESTORE A
 RTS
;
;
CHRIN:       ;MOST IMPORTANT KERNAL CALL
 JSR ROMSIN  ;DROPS THRU 
 JSR $FFCF
;(FT)
;
ROMOUT:
 PHA      ;SAVE AC
 LDA #$38
 SEI
 STA $01  ;KILL OP-SYSTEM
 PLA      ;RESTORE AC
 RTS
;
VIC_SCREEN_ON:
 JSR ROMSIN           ;.X UNHARMED
 LDA #27
 BNE VSO_JOIN
;
VIC_SCREEN_OFF:
 JSR ROMSIN
 LDA #11
VSO_JOIN:
 STA $D011
 BNE ROMOUT
;
CLR_BACKGROUND:
CLRBLK:
 JSR ROMSIN
 LDX #0
?CLRB_LOOP:
 LDA STCOLR
 STA $D800,X
 LDA TXCOLR
 STA $286
 STA $D84F,X
 STA $D900,X
 STA $DA00,X
 STA $DB00,X
 DEX
 BNE ?CLRB_LOOP
;
 BEQ ROMOUT
;
;
TOP_AREA:
 LDA TXCOLR
 PHA
 LDA BDRBAK
 STA TXCOLR
 JSR CLRBLK
 PLA
 STA TXCOLR
 RTS
;
;
