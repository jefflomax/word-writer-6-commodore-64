 EXTERN MACRO_LENGTH
 EXTERN MACRO_LIMIT
 EXTERN MACRO_1_START
 EXTERN MACRO_1_END 
 EXTERN MACRO_2_START
 EXTERN MACRO_2_END
 EXTERN STRTUP              ;BEGIN EXCESS CODE    ENDMOD
 EXTERN WHICH_MACRO
;
MAX_MACRO_LENGTH:  EQU 119
.LIST ON
;
; BASIC START
;
 ORG  $801
; ORG  $7FF
; DB $01,$08
 DB $24,8,$A
;
CBUF2: 
; 
